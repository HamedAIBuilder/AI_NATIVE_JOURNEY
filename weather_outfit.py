# Let's pretend we have a way to know the weather!
# For now, we'll just set it manually for our example.
weather = "rainy"

if weather == "sunny":
    print("It's sunny! I'll wear a t-shirt and shorts.")
else:
    print("It's not sunny. I'll grab a jacket and long pants.")

print("Time to head out!") 

# Let's pretend we're checking the weather for our umbrella decision
# We'll just say it's raining for now, but you could change this!
is_raining = False  # 'True' means yes, it IS raining

if is_raining == True:  # This is our 'IF' part
    print("Oh, look! It's raining. Better take my umbrella!")
else:  # This is our 'ELSE' part, for when it's NOT raining
    print("No rain today! No need for an umbrella.")

print("Time to start my day!") # This always prints, no matter what the weather is
